    {
        $data = book::with('materi')->select('id_materi')->where('id_mapel', '=', $id)->groupBy('id_materi')->get();
        $book = book::get();
    
        return view('submateri',[
            'data' => $data,
            'book' => $book
        ]);
    }

    public function pdfstream($id)
    {
        $data = book::where('id_buku', $id)->first();
        return view('/pdfview', [
            'file' => $data
        ]);
    }

